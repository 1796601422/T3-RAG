def build_messages(question: str, contexts: list[dict]) -> list[dict]:
    context_lines: list[str] = []
    for index, item in enumerate(contexts, start=1):
        location = [f"\u6587\u4ef6\uff1a{item['filename']}"]
        if item.get("page_no") is not None:
            location.append(f"\u9875\u7801\uff1a{item['page_no']}")
        if item.get("section_title"):
            location.append(f"\u6807\u9898\uff1a{item['section_title']}")
        if item.get("retrieval_note"):
            location.append(f"\u68c0\u7d22\u5907\u6ce8\uff1a{item['retrieval_note']}")
        context_lines.append(f"[{index}] {' | '.join(location)}\n{item['content'].strip()}")

    user_prompt = (
        f"\u95ee\u9898\uff1a\n{question}\n\n"
        f"\u53c2\u8003\u8bc1\u636e\uff1a\n{chr(10).join(context_lines)}\n\n"
        f"\u8bf7\u57fa\u4e8e\u53c2\u8003\u8bc1\u636e\u4f5c\u7b54\u3002"
        f"\u5982\u679c\u8bc1\u636e\u53ea\u652f\u6301\u90e8\u5206\u56de\u7b54\uff0c\u8bf7\u56de\u7b54\u5df2\u652f\u6301\u7684\u90e8\u5206\u5e76\u8bf4\u660e\u7f3a\u53e3\uff1b"
        f"\u53ea\u6709\u53c2\u8003\u8bc1\u636e\u4e0e\u95ee\u9898\u5b8c\u5168\u65e0\u5173\u65f6\uff0c\u624d\u76f4\u63a5\u8f93\u51fa\uff1a{FALLBACK_ANSWER}"
    )
    return [
        {"role": "system", "content": SYSTEM_PROMPT},
        {"role": "user", "content": user_prompt},
    ]
